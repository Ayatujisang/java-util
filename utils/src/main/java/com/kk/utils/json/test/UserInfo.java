public class UserInfo {
    private String name;
    private int age;

    private Date date = new Date();

    public UserInfo() { // require
    }

    public UserInfo(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public int getAgePlus() {
        return age + 1;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    @Override
    public String toString() {
        return "UserInfo [name=" + name + ", age=" + age + ", date="
                + date.toLocaleString() + "]";
    }

}